

pi = 22/7
def rectangle(l,b):
    area = l*b
    return area

def square(s):
    area = s * s
    return area

def triangle(b,h):
    area = 0.5 * b * h
    return area
def circle(r):
    area = pi * r * r
    return area

def parallelogram(b,h):
    area = b*h
    return area

def half_circle(r):
    area = (pi * r * r)*0.5
    return area
